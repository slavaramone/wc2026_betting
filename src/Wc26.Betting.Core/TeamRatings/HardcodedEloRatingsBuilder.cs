    private static readonly JsonSerializerOptions JsonOptions = new() { WriteIndented = true };

    public EloRatingSet Build()
    {
        var teams = Rows
            .Select(x => new EloTeamRating
            {
                Rank = x.Rank,
                Team = x.Team,
                NormalizedTeam = NormalizeToEloName(x.Team),
                Rating = x.Rating
            })
            .OrderBy(x => x.Rank)
            .ThenBy(x => x.Team, StringComparer.OrdinalIgnoreCase)
            .ToList();

        return new EloRatingSet
        {
            BuiltAtUtc = DateTimeOffset.UtcNow,
            AsOfDate = new DateOnly(2026, 5, 26),
            Source = "Hardcoded Elo ratings from user-provided table: Ratings and Statistics as of Tuesday May 26 2026",
            Teams = teams
        };
    }

    public async Task WriteAsync(EloRatingSet set, string modelsFolder, bool overwrite, CancellationToken cancellationToken)
    {
        var folder = Path.Combine(modelsFolder, "team-ratings");
        Directory.CreateDirectory(folder);

        var jsonPath = Path.Combine(folder, "hardcoded-elo-ratings.json");
        var csvPath = Path.Combine(folder, "hardcoded-elo-ratings.csv");

        if (!overwrite && (File.Exists(jsonPath) || File.Exists(csvPath)))
            throw new IOException($"Team-ratings output already exists in '{folder}'. Use --overwrite to replace it.");

        await File.WriteAllTextAsync(jsonPath, JsonSerializer.Serialize(set, JsonOptions), cancellationToken);

        var lines = new List<string> { "rank,team,normalized_team,rating,as_of_date,source" };
        lines.AddRange(set.Teams.Select(x => string.Join(',',
            x.Rank.ToString(CultureInfo.InvariantCulture),
            SimpleCsv.Escape(x.Team),
            SimpleCsv.Escape(x.NormalizedTeam),
            x.Rating.ToString(CultureInfo.InvariantCulture),
            set.AsOfDate.ToString("yyyy-MM-dd", CultureInfo.InvariantCulture),
            SimpleCsv.Escape(set.Source))));

        await File.WriteAllLinesAsync(csvPath, lines, cancellationToken);
    }

    public static string NormalizeToEloName(string value)
    {
        var v = value.Trim();
        return v switch
        {
            "USA" => "United States",
            "United States of America" => "United States",
            "Korea Republic" => "South Korea",
            "Korea, Republic of" => "South Korea",
            "IR Iran" => "Iran",
            "Türkiye" => "Turkey",
            "Côte d'Ivoire" => "Ivory Coast",
            "Cabo Verde" => "Cape Verde",
            "Bosnia & Herzegovina" => "Bosnia and Herzegovina",
            "Congo DR" => "DR Congo",
            "Czech Republic" => "Czechia",
            _ => v
        };
    }

